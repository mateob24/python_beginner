#definiendo una variable con snake-case
nombre_completo = "Mateo"

#concatenar con f-strings (también se puede con '+')
bienvenida = f"Hola {nombre_completo} ¿cómo estás?"
print(bienvenida)

#operadores de pertenencia (in / not in)
print("Mateo" in bienvenida) #true
print("Mateo" not in bienvenida) #false
